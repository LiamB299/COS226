/**
 * Specifies default values for options used in the TThread class
 */
public class TThreadOptions {
    /**
     * The number of iterations each thread must execute
     */
    public static int numRuns = 5;
    /**
     * Duration of sleep between thread iterations (in milliseconds)
     */
    public static int sleepTime = 50;
    /**
     * Amount of money to withdraw from the account
     */
    public static float withdrawAmount = 200;
    /**
     * Amount of money to deposit into the account
     */
    public static float depositAmount = 200;
}